from collections import namedtuple

Result = namedtuple('Result', 'value, next_pc')
Instruction = namedtuple('Instruction', 'opcode, num_param, writes, func')

decoder = {i.opcode: i for i in [
    Instruction('01', 2, True, lambda p: Result(p[0] + p[1], None)),
    Instruction('02', 2, True, lambda p: Result(p[0] * p[1], None)),
    Instruction('03', 0, True, lambda p: Result(next(INPUT), None)),
    Instruction('04', 1, False, lambda p: Result(print(p[0]), None)),
    Instruction('05', 2, False, lambda p: Result(None, p[1] if p[0] else None)),
    Instruction('06', 2, False, lambda p: Result(None, None if p[0] else p[1])),
    Instruction('07', 2, True, lambda p: Result(int(p[0] < p[1]), None)),
    Instruction('08', 2, True, lambda p: Result(int(p[0] == p[1]), None))
]}

modes = {
    '0': lambda p: MEM[p],
    '1': lambda p: p
}

def decode(mem):
    global pc
    mem = '000000' + str(mem)
    instr = decoder[mem[-2:]]
    pc += 1
    accessor = list(map(modes.__getitem__, mem[-3:-(3 + instr.num_param):-1]))
    return instr, accessor

def step():
    global pc
    instr, accessor = decode(MEM[pc])
    params = [a(m) for a, m in zip(accessor, MEM[pc:pc + instr.num_param])]
    address = MEM[pc + instr.num_param:pc + instr.num_param + instr.writes][0] if instr.writes else None
    result = instr.func(params)
    if instr.writes: MEM[address] = result.value
    pc = result.next_pc if result.next_pc else pc + instr.num_param + instr.writes

def run(PRG, IN):
    global pc, MEM, INPUT
    MEM = PRG[:]
    INPUT = iter(IN)
    pc = 0
    while MEM[pc] != 99:
        step()

